pub use ethereum_types::{U256};

#[tokio::test]
async fn run() {
    let x = U256([11000000000000000000, 0, 0, 0]);
    let y = U256([13399722918938673152, 7145508105175220139, 29, 0]);
    let product = x * y;
    // let expected = U256::from((17, 9666297223066687219, 7425695065611822185, 14699749183737298944));
    let expected = U256([14699749183737298944, 7425695065611822185, 9666297223066687219, 17]);
    assert_eq!(product, expected);
}
